package com.stealthsoftwareinc.sst;

/* begin_imports */

import java.util.ArrayList;
import java.util.concurrent.atomic.AtomicInteger;

/* end_imports */

/**
 *
 * @since SST&nbsp;X.X.X (XXXX-XX-XX)
 *
 * @examples
 * none
 *
 * @notablechanges
 * none
 */

public final class TaskResultList<T>
{

/**
 *
 * @since SST&nbsp;X.X.X (XXXX-XX-XX)
 *
 * @examples
 * none
 *
 * @notablechanges
 * none
 */

public final AtomicInteger countdown;

/**
 *
 * @since SST&nbsp;X.X.X (XXXX-XX-XX)
 *
 * @examples
 * none
 *
 * @notablechanges
 * none
 */

public final ArrayList<T> results;

/**
 *
 * @since SST&nbsp;X.X.X (XXXX-XX-XX)
 *
 * @examples
 * none
 *
 * @notablechanges
 * none
 */

public TaskResultList(
  final int count
) {
  this.countdown = new AtomicInteger(count);
  this.results = new ArrayList<T>(count);
  for (int i = 0; i != count; ++i) {
    this.results.add(null);
  }
}

}
