package com.stealthsoftwareinc.sst;

/* begin_imports */

import java.lang.String;

/* end_imports */

/**
 * Thrown when a nonnull reference argument or a nonnull native pointer
 * argument does not appear to be pointing to an object of an
 * appropriate type.
 * <p>
 * This class corresponds to the
 * <code>SST_WILD_POINTER</code>
 * status code.
 * For more information, see the Status Codes section of the SST manual.
 * </p>
 *
 * @since SST&nbsp;X.X.X (XXXX-XX-XX)
 *
 * @examples
 * none
 *
 * @notablechanges
 * none
 */

public class WildPointerStatus
extends NonnullPointerStatus
{

/**
 * Constructs a
 * <code>WildPointerStatus</code>
 * exception without a detail message and with an uninitialized cause.
 *
 * @since SST&nbsp;X.X.X (XXXX-XX-XX)
 *
 * @examples
 * none
 *
 * @notablechanges
 * none
 */

public WildPointerStatus(
) {
  this(null);
}

/**
 * Constructs a
 * <code>WildPointerStatus</code>
 * exception with or without a detail message and with an uninitialized
 * cause.
 *
 * @param message
 * the detail message, or
 * <code>null</code>
 * for no detail message
 *
 * @since SST&nbsp;X.X.X (XXXX-XX-XX)
 *
 * @examples
 * none
 *
 * @notablechanges
 * none
 */

public WildPointerStatus(
  final String message
) {
  super(message);
}

}
