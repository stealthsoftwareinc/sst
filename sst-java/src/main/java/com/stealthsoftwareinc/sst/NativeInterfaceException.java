package com.stealthsoftwareinc.sst;

/* begin_imports */

import java.lang.String;

/* end_imports */

/**
 * Thrown when a native method fails because a function from the Java
 * Native Interface failed.
 *
 * @since SST&nbsp;0.1.0 (2016-05-11)
 *
 * @examples
 * none
 *
 * @notablechanges
 * none
 */

public class NativeInterfaceException
extends RuntimeException
{

/**
 * Constructs a
 * <code>NativeInterfaceException</code>
 * without a detail message and with an uninitialized cause.
 *
 * @since SST&nbsp;0.1.0 (2016-05-11)
 *
 * @examples
 * none
 *
 * @notablechanges
 * none
 */

public NativeInterfaceException(
) {
  this(null);
}

/**
 * Constructs a
 * <code>NativeInterfaceException</code>
 * with or without a detail message and with an uninitialized cause.
 *
 * @param message
 * the detail message, or
 * <code>null</code>
 * for no detail message
 *
 * @since SST&nbsp;0.1.0 (2016-05-11)
 *
 * @examples
 * none
 *
 * @notablechanges
 * none
 */

public NativeInterfaceException(
  final String message
) {
  super(message);
}

}
